# my name is kendall today's date is 9/11/2025
# This program shows you how to remove white space using lstrip() etc

name = "\t\n  Kendall  \n\t" # Name with tabs and newlines

print("Original with whitespace:")
print(name)

print("\nUsing  lstrip():")
print(name.lstrip())

print("\nUsing  rstrip():")
print(name.rstrip())

print("\nUsing strip():")
print(name.strip())